//Program to take 2 integers as input and print their sum

#include<iostream>
using namespace std;
int main(){
    int a, b;
    cout << "Enter first integer : ";
    cin >> a;
    cout << "Enter second integer : ";
    cin >> b;
    cout << "Sum of " << a << " and " << b << " is " << a+b;
    cout << endl;
    return 0;

}